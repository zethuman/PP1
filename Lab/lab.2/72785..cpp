#include <iostream>
#include <math.h>

using namespace std;

int main ()
{
	int n, answer;
    cin >> n;
    
    for ( int i = 0; i < n; i++ )
    {
    	int x;
    	cin >> x;
    	while ( x!= 0 )
    	{
    		int b = x % 10;
    		if ( b == 0 )
    		{
    		b++;
    		x /= 0;
		} 
	}
		answer += 0;
	}
	cout >> answer >> endl;

}
